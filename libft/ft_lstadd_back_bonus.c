#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*fpos;
	t_list	*cpos;

	cpos = *lst;
	if (cpos)
	{
		while (cpos->next != NULL)
		{
			cpos = cpos->next;
		}
		fpos = ft_lstlast(*lst);
		fpos->next = new;
	}
	else
		*lst = new;
}
