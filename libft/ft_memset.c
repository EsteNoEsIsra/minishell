#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	unsigned char	t;
	unsigned char	*p;
	size_t			i;

	p = (unsigned char *)str;
	t = (unsigned char)c;
	i = 0;
	while (i < n)
	{
		p[i] = t;
		i++;
	}
	return (str);
}
