#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*pos;

	while (*lst != NULL)
	{
		del((*lst)->content);
		pos = *lst;
		*lst = pos->next;
		free(pos);
	}
	*lst = NULL;
}
